// Common duration constants for animations, delays, and transitions

class ProjectDuration {
  const ProjectDuration._();

  // Milliseconds
  static const Duration xShort = Duration(milliseconds: 100);
  static const Duration short = Duration(milliseconds: 200);
  static const Duration medium = Duration(milliseconds: 300);
  static const Duration longMs = Duration(milliseconds: 400);
  static const Duration xLongMs = Duration(milliseconds: 500);

  // Seconds
  static const Duration oneSecond = Duration(seconds: 1);
  static const Duration twoSeconds = Duration(seconds: 2);
  static const Duration threeSeconds = Duration(seconds: 3);
}


